    public static String samsungExerciseIdToUid(int i) {
        String str = (String) SAMSUNG_TO_MFP_EXERCISE.get(Integer.valueOf(i));
        if (str == null) {
            return null;
        }
        if (!Strings.notEmpty(str)) {
            str = DEFAULT_EXERCISE_UID;
        }
        return str;
    }

    public static int uidToSamsungExerciseId(String str) {
        if (MFP_TO_SAMSUNG_EXERCISE.containsKey(str)) {
            return ((Integer) MFP_TO_SAMSUNG_EXERCISE.get(str)).intValue();
        }
        for (Entry entry : SAMSUNG_TO_MFP_EXERCISE.entrySet()) {
            if (Strings.equals(str, (String) entry.getValue())) {
                return ((Integer) entry.getKey()).intValue();
            }
        }
        return 0;
    }

